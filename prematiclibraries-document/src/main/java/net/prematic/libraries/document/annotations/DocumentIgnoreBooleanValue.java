package net.prematic.libraries.document.annotations;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * This annotation is used, when the field in an boolean and should be ignored and skipped if value is equal with ignore.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.FIELD)
public @interface DocumentIgnoreBooleanValue {

    boolean ignore();
}